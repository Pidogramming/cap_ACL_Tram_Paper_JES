function massTable =  table_tramMass(obj)
% Creates a 3 Columns table of Tram mass info
tableData = {...
    'Rotary Allowance',...
    sprintf('%.2g', obj.rotaryAllowance),...
    '-';...                 % end row 1
    
    'Mass Tare', ...
    sprintf('%.4g', obj.massTare/ 1e3),...
    '[metric ton]';...      % end row 2
    
    'Mass People', ...
    sprintf('%.4g', obj.massPeople/ 1e3),...
    '[metric ton]';...
    
    'Mass OBESD', ...
    sprintf('%.4g', obj.massOBESD/ 1e3),...
    '[metric ton]';...
    
    'Mass Total', ...
    sprintf('%.4g', obj.massTotal/ 1e3),...
    '[metric ton]';...
    
    'Mass Effective', ...
    sprintf('%.4g', obj.massEffective/ 1e3),...
    '[metric ton]';...
    };
massTable = table(tableData);
massTable.Properties.VariableNames = {'Tram_Mass'};
end